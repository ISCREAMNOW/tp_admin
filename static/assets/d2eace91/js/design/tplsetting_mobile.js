/*
 * 处理手机端装修模块拖拽的js
 */

$(function() {
	/*
	 * 设置初始化变量
	 */
	var flag = true;
	var temp = '';
	var uid = '';

	if (typeof (topic_id) == 'undefined') {
		topic_id = 0;
	}
	/* -------------------- 主区域拖拽事件start ---------------------- */

	/**
	 * 放置事件
	 */
	$('.SZY-TEMPLATE-MAIN-CONTAINER').droppable({
		activeClass: "dragactive",
		hoverClass: "draghover",
		accept: ":not(.ui-sortable-helper)", // Reject clones generated by
		drop: function(e, ui) {
			if (flag == true) {
				$('.SZY-TEMPLATE-MAIN-CONTAINER').find('li.drag').addClass('hide');
				$.ajax({
					type: 'get',
					url: 'select-template',
					async: false,
					dataType: "json",
					data: {
						id: ui.draggable[0].id,
						code: ui.draggable['context']['dataset']['code'],
						sort: $(".drop-item").size() + 1,
						page: page,
						topic_id: topic_id
					},
					beforeSend: function() {
						$.loading.start();
					},
					success: function(result) {
						temp = result.data;
						uid = result.uid;
						$.loading.stop();
						$.msg(result.message);
						flag = false;
					}
				});
			}
		}
	}).sortable({
		items: '.drop-item',
		scroll: true,
		tolerance: 'intersect',
		opacity: 0.6,
		distance: 50,
		placeholder: 'position-box',
		sort: function() {
			// 获取由 droppable 与 sortable 交互而加入的条目
			// 使用 connectWithSortable 可以解决这个问题，但不允许您自定义 active/hoverClass 选项
			$(this).removeClass("active");
		},
		stop: function(event, ui) {
			flag = true;
			if (ui.item[0].id == '') {
				if (temp) {
					var $el = $(temp);
					var i = $(this).find('li.drag').prevAll('div.drop-item').size() - 1;
					if ($(this).find('.drop-item').size() > 0) {
						if (i < 0) {
							$(this).find('.drop-item').eq(0).before($el);
						} else {
							$(this).find('.drop-item').eq(i).after($el);
						}
					} else {
						$(this).append($el);
					}

					refreshTpl(page, uid);
				}
				$(this).find('li.drag').remove();
			}
			savesort(page, ui.item.attr('id'));
		}
	});

	$('.drag').draggable({
		appendTo: 'body',
		connectToSortable: ".SZY-TEMPLATE-MAIN-CONTAINER",
		helper: 'clone',
		cursor: 'move',
		revert: 'invalid',
		opacity: 0.6,
		zIndex: 99
	});

	$(".SZY-TEMPLATE-MAIN-CONTAINER").disableSelection();

	/* -------------------- 主区域拖拽事件end ---------------------- */

	/* -------------------- 公共方法 ------------------------ */

	/**
	 * 页面发布
	 */
	$('.SZY-TPL-RELEASE').click(function() {
		var target = $(this);
		$.ajax({
			type: 'get',
			async: false, // 同步请求
			url: 'setting',
			dataType: 'json',
			data: {
				page: page,
				topic_id: topic_id
			},
			beforeSend: function() {
				$.loading.start();
			},
			success: function(result) {
				$.loading.stop();
				if (result.code == 0 && result.url != null) {
					if ($.modal(target)) {
						$.modal(target).show();
					} else {
						var modal = $.modal({
							title: "提示",
							trigger: target,
							content: "<div class='success-box'><h1 class='success-title'>发布成功</h1><div class='success-description'>页面连接为： " + result.url + "</div><div class='success-box-bootom'><div class='pic-wrapper'><img src='" + result.qrcode + "'></div><div class='pic-info'>用手机扫描左侧二维码，查看装修效果</div></div></div>"
						});
					}

				} else {
					$.msg(result.message);
				}
			}
		});
	});

	/**
	 * 整站改色模块
	 */
	$('.SZY-SITE-STYLE').click(function() {
		$.loading.start();
		modal = $.modal($(this));
		if (modal) {
			modal.show();
			$.loading.stop();
		} else {
			modal = $.modal({
				title: '自定义风格',
				trigger: $(this),
				ajax: {
					url: '/system/config/index?group=mobile_site_style',
				},

			});
		}
	});
	
	
	/**
	 * 模板备份
	 */
	$('.SZY-TPL-BACKUP').click(function() {
		$.loading.start();
		modal = $.modal($(this));
		if (modal) {
			modal.show();
			$.loading.stop();
		} else {
			modal = $.modal({
				title: '模板备份',
				trigger: $(this),
				ajax: {
					url: '/site/tpl-backup?design_page=' + page,
					data: {
						action: 'add',
						output: 1,
					}
				},

			});
		}
	});

	/**
	 * 使用备份
	 */
	$('.SZY-TPL-USE').click(function() {
		$.loading.start();
		modal = $.modal($(this));
		if (modal) {
			modal.show();
			$.loading.stop();
		} else {
			modal = $.modal({
				title: '使用备份',
				trigger: $(this),
				ajax: {
					url: '/site/tpl-backup.html',
					data: {
						action: 'list',
						output: 1,
						design_page: page,
						search_id : $(this).data('id')
					}
				},

			});
		}
	});

});

$('.SZY-TEMPLATE-MAIN-CONTAINER').on('mouseover', '.drop-item', function() {
	$('.SZY-TEMPLATE-MAIN-CONTAINER').find('.drop-item').removeClass('current');
	$(this).addClass('current');
	$("#mobile_helper").find('.columnNameText').removeClass('selected');
	$("#mobile_helper").find("#columnNameText_" + $(this).attr('id')).addClass('selected');
});

$('.SZY-TEMPLATE-MAIN-CONTAINER').on('mouseout', '.drop-item', function() {
	$(this).removeClass('current');
	$("#mobile_helper").find('.columnNameText').removeClass('selected');
});
function savesort(page, uid) {
	var arr = [];
	$.each($(".drop-item"), function(i, v) {
		var tpl = {};
		tpl.uid = $(v).attr('id');
		tpl.sort = i + 1;
		if (tpl.uid && tpl.sort) {
			arr.push(tpl);
		}
	});
	if (arr.length > 0) {
		$.ajax({
			type: 'get',
			url: 'sort',
			data: {
				tpl: arr,
			},
			success: function(result) {
				// 调用模板设置助手
				getHelper(uid);
			}
		});
	} else {
		getHelper(uid);
	}
}

function savesortHelper() {
	var arr = [];
	$.each($("#mobile_helper").find('tbody tr'), function(i, v) {
		var tpl = {};
		tpl.uid = $(v).data('id');
		tpl.sort = i + 1;
		if (tpl.uid && tpl.sort) {
			arr.push(tpl);
		}
	});
	$.ajax({
		type: 'post',
		url: 'sort',
		data: {
			tpl: arr,
		},
		success: function(result) {
			var $item = $('<div></div>');
			$.each(arr, function(i, v) {
				$item.append($('.SZY-TEMPLATE-MAIN-CONTAINER').find("#" + v.uid));
			});
			$('.SZY-TEMPLATE-MAIN-CONTAINER').html($item);
		}
	});
}

function clearTplData() {
	chk_value = [];
	data = [];
	data_m = {};
}

function delTpls(uid) {
	$.confirm("您确定要删除这个模块吗?", function(s) {
		if (s) {
			$.ajax({
				type: 'get',
				url: 'delete-tpls',
				dataType: 'json',
				data: {
					uid: uid,
				},
				beforeSend: function() {
					$.loading.start();
				},
				success: function(result) {
					$('#' + uid).detach();
					// 从新排序
					$.loading.stop();
					savesort(page, uid);
					$.msg(result.message);
				}
			});
		}
	});
}

