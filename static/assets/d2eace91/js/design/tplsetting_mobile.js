// 设置是否有效
function setIsValidTpls(uid) {
	$.ajax({
		type: 'get',
		url: 'valid-tpls',
		dataType: 'json',
		data: {
			uid: uid,
		},
		beforeSend: function() {
			$.loading.start();
		},
		success: function(result) {
			refreshTpl(result.data.page, result.data.uid);
			$.msg(result.message);
			$.loading.stop();
		}
	});
}

// 上下交换位置
function setSortTpls(uid, move) {
	var tpl = $("#" + uid);
	if (move == 'up') {
		var tplprevs = tpl.prevAll(".drop-item");
		var tplprev = tplprevs[0];
		if (tplprev) {
			tpl.insertBefore($("#" + tplprev.id));
			$('.special-item').mCustomScrollbar('scrollTo', tpl.position().top);
		} else {
			$.msg("已经到顶了!");
		}
	}
	if (move == 'down') {
		var tplnexts = tpl.nextAll(".drop-item");
		var tplnext = tplnexts[0];
		if (tplnext) {
			$("#" + tplnext.id).insertBefore(tpl);
			$('.special-item').mCustomScrollbar('scrollTo', tpl.position().top);
		} else {
			$.msg("已经到底了!");
		}
	}
	savesort(page, uid);
}

/*
 * 模板设置助手js
 */
function getHelper(id) {

	/* 模板设置助手js */

	// 生成页面导航助手
	$("#mobile_helper").find("tbody").html("");
	var count = 0;
	$(".drop-item").each(function(i, v) {
		var cardinal = ''
		if (i % 2 == 1)
			cardinal = "class='cardinal'";
		var title = $(this).data("tpl_name");
		var uid = $(this).attr('id');
		var selected_class = '';
		var tpl_type = $(this).data("tpl_type");
		var is_valid_class = '';
		if ($(this).data('is_valid') > 0) {
			is_valid_class = "<i class='fa fa-check-circle'></i>显示";
		} else {
			is_valid_class = "<i class='fa fa-times-circle-o'></i>隐藏";
		}
		var element = $($.parseHTML("<tr " + cardinal + " data-id='" + uid + "'><td><span class='columnParent'>" + tpl_type + "</span><span class='columnNameText' id='columnNameText_" + uid + "'>" + title + "</span></td>" + "<td><span class='ico-switch open' onclick=setIsValidTpls('" + uid + "')> " + is_valid_class + "</span></td>" + "<td class='handle'><a href=javascript:delTpls('" + uid + "') title='删除'><i class='fa fa-trash-o'></i></a></a></a><a class='handle' href=javascript:setSortTpls('" + uid + "','up') title='上移'><i class='fa fa-arrow-circle-o-up'></i></a><a class='handle' href=javascript:setSortTpls('" + uid + "','down') title='下移'><i class='fa fa-arrow-circle-o-down'></i></a></td></tr>"));
		if (id != '' && id == uid) {
			$(element).find('.columnNameText').addClass('selected');
		}
		$("#mobile_helper").find("tbody").append(element);

		var target = this;
		$(element).click(function() {
			var position = 0;
			$(this).find('.columnNameText').addClass('selected');
			$('.SZY-TEMPLATE-MAIN-CONTAINER').find('.drop-item').removeClass('current');
			$('.SZY-TEMPLATE-MAIN-CONTAINER').find("#" + uid).addClass('current');
			$(this).siblings().find('.columnNameText').removeClass('selected');
			var top = $('#' + uid).position().top - 150;
			if (top < 0) {
				top = 0;
			}
			$('.special-item').mCustomScrollbar('scrollTo', top);

		});
		count++;
	});

	$("#mobile_helper").find('tbody').sortable({
		revert: true,
		tolerance: 'intersect',
		opacity: 0.6,
		out: function(event, ui) {
			savesortHelper();
		}
	});

	// $("#helper_tool_nav").find(".count").html(count);
}

var _timer = {};
function delay_till_last(id, fn, wait) {

	if (_timer[id]) {
		window.clearTimeout(_timer[id]);
		delete _timer[id];
	}

	return _timer[id] = window.setTimeout(function() {
		fn();
		delete _timer[id];
	}, wait);
}

// 刷新当前模板
function refreshTpl(page, uid) {
	clearTplData();
	$.ajax({
		type: 'get',
		async: false, // 同步请求
		url: "tpl-ref",
		dataType: 'json',
		data: {
			page: page,
			uid: uid,
		},
		beforeSend: function() {
			// $.loading.start();
		},
		success: function(tpls) {
			if (uid == undefined || uid == '') {
				$('.SZY-TEMPLATE-MAIN-CONTAINER').html('');
			}
			$.each(tpls, function(index, value) {
				var $el = $(value.file);
				var is_valid_class = '';
				if (value.is_valid > 0) {
					is_valid_class = 'fa fa-check-circle-o';
				} else {
					is_valid_class = 'fa fa-times-circle-o';
				}
				$el.append($('<div class="item_type">' + value.tpl_name + '</div>'));
				var $handle = $('<div class="handle"></div>');

				$handle.append($('<a href="javascript:;"><i class="fa fa-arrow-circle-o-up"></i>上移</a>').click(function() {
					setSortTpls(value.uid, 'up');
				}));
				$handle.append($('<a href="javascript:;"><i class="fa fa-arrow-circle-o-down"></i>下移</a>').click(function() {
					setSortTpls(value.uid, 'down');
				}));

				$handle.append($('<a href="javascript:;"><i class="' + is_valid_class + '"></i>' + value.format_is_valid + '</span></a>').click(function() {
					setIsValidTpls(value.uid);
				}));
				$handle.append($('<a href="javascript:;"><i class="fa fa-trash-o"></i>删除</a>').click(function() {
					delTpls(value.uid);
				}));
				$el.append($handle);
				if (uid == undefined || uid == '') {
					$('.SZY-TEMPLATE-MAIN-CONTAINER').append($el);
				} else {
					$('.SZY-TEMPLATE-MAIN-CONTAINER').find("#" + value.uid).replaceWith($el);
				}
			});
			// 图片缓载
			$.imgloading.loading();
			// 生成模板助手
			getHelper(uid);
		}
	});
}

// 刷新头部
function ajaxRender(page, tpl, container) {
	if (tpl == undefined || tpl == '') {
		$.go(window.location);
		return;
	}
	if (topic_id > 0) {
		page = page + topic_id;
	}
	$.ajax({
		type: 'get',
		url: "ajax-render",
		dataType: 'json',
		data: {
			page: page,
			tpl: tpl
		},
		success: function(result) {
			if (result.code == 0) {
				$(container).html($.parseHTML(result.data));
			}

		}
	});
}